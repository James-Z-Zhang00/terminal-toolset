#####################################
# Some shortened commands
#####################################

# 1. Basic operations
alias cle="clear"
alias o="open"

# 2. Go back to the previous directory quicker
alias cc="cd .."
alias ccc="cd ../.."
alias cccc="cd ../../.."

# 3. Shorthand to open frequently used apps and folders
alias vsc="open -a 'Visual Studio Code.app'" # Use the Mac OS built-in open command so it will always work without enabling the code command or update PATH
alias vscn='code -n'
alias vscr='code -r'
alias cursor="open -a 'Cursor.app'"
alias gc="open -a 'Google Chrome.app'"
alias Desk="cd ~/Desktop"
